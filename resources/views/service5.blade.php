Các điều kiện về khí hậu như trên đã tích hợp thành môi trường thuận lợi cho muỗi, côn trùng gây hại sinh sôi, nảy nở phát triển nhanh. 
Muỗi nói riêng, côn trùng gây hại nói chung là vector truyền nhiễm, lây lan các dịch bệnh nguy hiểm cho con người như sốt xuất huyết, sốt rét, sốt vàng, giun chỉ, bạch huyết, viêm não Nhật Bản, da phơi nhiễm…
Muỗi truyền bệnh sốt xuất huyết, sốt vang và một số bệnh do virut khác là muỗi vằn, tên khoa học (Aedes aegypti), sống ở vùng nhiệt đới trong đó có Việt Nam. 
Chúng thường trú ngụ, đẻ trứng ở gần nhà, thường thích đốt máu người vào buổi sáng hoặc buổi chiều tối.

 
 

